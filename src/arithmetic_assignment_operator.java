public class arithmetic_assignment_operator {
    public static void main(String args[]){
        int a=123;
        System.out.println(a);
        a+=10;
        System.out.println(a);
        a-=10;
        System.out.println(a);
        a*=10;
        System.out.println(a);
        a/=10;
        System.out.println(a);
        a%=10;
        System.out.println(a);

    }
}
